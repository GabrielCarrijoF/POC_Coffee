public class Coffe {

  public Integer id;
  public String name;
  public String countryOfOrigin;
  public Integer price;

  public Coffe() {
  }

  public Coffe(Integer id, String name, String countryOfOrigin, Integer price) {
    this.id = id;
    this.name = name;
    this.countryOfOrigin = countryOfOrigin;
    this.price = price;
  }
}